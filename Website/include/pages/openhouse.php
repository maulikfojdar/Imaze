	<div class="main_content">
		<b>'Technology and Governance-an eternal and happy matrimony or diabolic deadlock?'</b>

		<p>Ideas are always arrived upon by consensus, and consensus is rarely achieved without discussion.Keeping this in mind, IMAZE 11 is bringing together The Titans from two of the most vital domains of the society-Technology and Governance. 
		We saw these two working as a well oiled machine in recent General Elections, which saw the introduction of Electronic Polling System.
		Such a system in future, coupled with initiatives like "Aadhar"(Unique Identification) project makes the working of two symbiotic.</p>
		
		<p>The panel will mainly discuss on the need of interdependency between technology and governance. 
		The attendee will witness the views of invited ignited minds on the same. Aiding this discussion will be the fourth estate i.e. media and luminaries from the field of academics. They will act as catalyst to the discussion providing opinions and suggestions. 
		Thus the ideas and the concluding thoughts of the symposium will lead to a better society, a better nation and thus a better world.</p>
